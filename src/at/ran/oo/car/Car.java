package at.ran.oo.car;

public class Car {
    private String color;
    private int serialNumber;

    public Car(String color, int serialNumber) {

        this.color = color;
        this.serialNumber = serialNumber;
    }

    public String getColor() {
        return color;
    }



    public int getSerialNumber() {
        return serialNumber;
    }


}
